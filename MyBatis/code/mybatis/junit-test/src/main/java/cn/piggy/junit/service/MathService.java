package cn.piggy.junit.service;

public class MathService {

    /**
     * 求和的业务方法
     * @param a
     * @param b
     * @return
     */
    public int sum(int a,int b){
        return a+b;
    }

    /**
     * 求减的业务方法
     * @param a
     * @param b
     * @return
     */
    public int sub(int a,int b){
        return a-b;
    }
}
